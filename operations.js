function add (num1, num2) {
     let sum = num1 + num2
     return sum
}

function subtract(num1, num2) {
     let res = num1 - num2
     return res
}

function multiply(num1, num2) {
     let mul = num1 * num2
     return mul
}

function divide(num1, num2) {
     let div = num1 / num2
     return div
}

module.exports = {add, subtract, multiply, divide}